import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = path.resolve(fileURLToPath(new URL('..', import.meta.url)));
const DATA_DIR = path.join(ROOT, 'src', 'data');
const OUT_FILE = process.argv[2]
  ? path.resolve(process.argv[2])
  : path.join(ROOT, '.tmp', 'd1-site-data-seed.sql');

function sql(value) {
  if (value === null || value === undefined) return 'NULL';
  return `'${String(value).replace(/'/g, "''")}'`;
}

async function main() {
  const files = (await fs.readdir(DATA_DIR)).filter((file) => file.endsWith('.json')).sort();
  const now = new Date().toISOString();
  const statements = [
    '-- Generated by scripts/export-site-data-d1-sql.mjs',
    `-- Files: ${files.length}`,
    `-- Generated at: ${now}`,
  ];

  for (const file of files) {
    const raw = await fs.readFile(path.join(DATA_DIR, file), 'utf-8');
    const content = JSON.stringify(JSON.parse(raw), null, 2);
    statements.push(`
INSERT INTO site_data (key, content, updated_at)
VALUES (${sql(file)}, ${sql(content)}, ${sql(now)})
ON CONFLICT(key) DO NOTHING;`);
  }

  statements.push('');
  await fs.mkdir(path.dirname(OUT_FILE), { recursive: true });
  await fs.writeFile(OUT_FILE, statements.join('\n'), 'utf-8');
  console.log(`Generated ${path.relative(ROOT, OUT_FILE)} with ${files.length} data files.`);
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
