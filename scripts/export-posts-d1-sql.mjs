import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = path.resolve(fileURLToPath(new URL('..', import.meta.url)));
const BLOG_DIR = path.join(ROOT, 'src', 'content', 'blog');
const OUT_FILE = process.argv[2]
  ? path.resolve(process.argv[2])
  : path.join(ROOT, '.tmp', 'd1-posts-seed.sql');

const MAIN_KEYS = new Set([
  'title',
  'description',
  'content',
  'category',
  'author',
  'image',
  'imageAlt',
  'image_alt',
  'pubDate',
  'updatedDate',
  'scheduledAt',
  'status',
  'draft',
]);

function parseScalar(value) {
  const raw = value.trim();
  if (raw === '') return '';
  if (raw === 'true') return true;
  if (raw === 'false') return false;
  if (raw === 'null') return null;
  if (/^-?\d+(\.\d+)?$/.test(raw)) return Number(raw);

  if ((raw.startsWith('"') && raw.endsWith('"')) || (raw.startsWith("'") && raw.endsWith("'"))) {
    try {
      return JSON.parse(raw);
    } catch {
      return raw.slice(1, -1);
    }
  }

  if (raw.startsWith('[') && raw.endsWith(']')) {
    return raw
      .slice(1, -1)
      .split(',')
      .map((item) => parseScalar(item))
      .filter((item) => item !== '');
  }

  return raw;
}

function parseFrontmatter(markdown, fileName) {
  const match = markdown.match(/^---\r?\n([\s\S]*?)\r?\n---\r?\n?/);
  if (!match) {
    throw new Error(`Missing frontmatter in ${fileName}`);
  }

  const frontmatter = {};
  for (const line of match[1].split(/\r?\n/)) {
    if (!line.trim() || line.trimStart().startsWith('#') || /^\s/.test(line)) continue;
    const keyValue = line.match(/^([A-Za-z0-9_-]+):\s*(.*)$/);
    if (!keyValue) continue;
    frontmatter[keyValue[1]] = parseScalar(keyValue[2]);
  }

  return {
    frontmatter,
    content: markdown.slice(match[0].length).trim(),
  };
}

function toIsoOrNull(value) {
  if (!value) return null;
  const date = new Date(value);
  return Number.isNaN(date.getTime()) ? String(value) : date.toISOString();
}

function bool(value) {
  if (typeof value === 'boolean') return value;
  if (typeof value === 'string') return value.toLowerCase() === 'true';
  return Boolean(value);
}

function sql(value) {
  if (value === null || value === undefined) return 'NULL';
  if (typeof value === 'number') return String(value);
  return `'${String(value).replace(/'/g, "''")}'`;
}

function normalizeStatus(frontmatter, scheduledAt, draft) {
  const raw = String(frontmatter.status || '').toLowerCase();
  if (['draft', 'scheduled', 'published', 'archived'].includes(raw)) return raw;
  if (scheduledAt) return 'scheduled';
  return draft ? 'draft' : 'published';
}

async function main() {
  const files = (await fs.readdir(BLOG_DIR)).filter((file) => file.endsWith('.md')).sort();
  const now = new Date().toISOString();
  const statements = [
    '-- Generated by scripts/export-posts-d1-sql.mjs',
    `-- Posts: ${files.length}`,
    `-- Generated at: ${now}`,
  ];

  for (const file of files) {
    const slug = file.replace(/\.md$/, '');
    const sourcePath = `src/content/blog/${file}`;
    const raw = await fs.readFile(path.join(BLOG_DIR, file), 'utf-8');
    const { frontmatter, content } = parseFrontmatter(raw, file);
    const scheduledAt = toIsoOrNull(frontmatter.scheduledAt);
    const draft = bool(frontmatter.draft);
    const status = normalizeStatus(frontmatter, scheduledAt, draft);
    const publishedAt = status === 'published' ? toIsoOrNull(frontmatter.pubDate) || now : null;
    const meta = { sourcePath };

    for (const [key, value] of Object.entries(frontmatter)) {
      if (!MAIN_KEYS.has(key)) meta[key] = value;
    }

    const values = [
      slug,
      frontmatter.title || slug,
      frontmatter.description || '',
      content,
      frontmatter.category || 'divulgacao',
      frontmatter.author || '',
      frontmatter.image || '',
      frontmatter.imageAlt || frontmatter.image_alt || '',
      frontmatter.pubDate || '',
      frontmatter.updatedDate || null,
      scheduledAt,
      publishedAt,
      status,
      status === 'scheduled' ? 1 : draft ? 1 : 0,
      JSON.stringify(meta),
      now,
      now,
    ].map(sql);

    statements.push(`
INSERT INTO posts (
  slug, title, description, content, category, author, image, image_alt,
  pub_date, updated_date, scheduled_at, published_at, status, draft,
  meta_json, created_at, updated_at
) VALUES (
  ${values.join(',\n  ')}
)
ON CONFLICT(slug) DO UPDATE SET
  title = excluded.title,
  description = excluded.description,
  content = excluded.content,
  category = excluded.category,
  author = excluded.author,
  image = excluded.image,
  image_alt = excluded.image_alt,
  pub_date = excluded.pub_date,
  updated_date = excluded.updated_date,
  scheduled_at = excluded.scheduled_at,
  published_at = excluded.published_at,
  status = excluded.status,
  draft = excluded.draft,
  meta_json = excluded.meta_json,
  updated_at = excluded.updated_at;`);

    if (status === 'scheduled' && scheduledAt) {
      statements.push(`
INSERT INTO scheduled_jobs (post_slug, job_type, run_at, status, attempts, created_at, updated_at)
VALUES (${sql(slug)}, 'publish_post', ${sql(scheduledAt)}, 'pending', 0, ${sql(now)}, ${sql(now)})
ON CONFLICT(post_slug, job_type) DO UPDATE SET
  run_at = excluded.run_at,
  status = 'pending',
  updated_at = excluded.updated_at;`);
    } else {
      statements.push(`DELETE FROM scheduled_jobs WHERE post_slug = ${sql(slug)} AND job_type = 'publish_post';`);
    }
  }

  statements.push('');
  await fs.mkdir(path.dirname(OUT_FILE), { recursive: true });
  await fs.writeFile(OUT_FILE, statements.join('\n'), 'utf-8');
  console.log(`Generated ${path.relative(ROOT, OUT_FILE)} with ${files.length} posts.`);
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
